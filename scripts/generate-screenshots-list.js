import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const screenshotsDir = path.join(__dirname, '../public/screenshots');
const outputFile = path.join(__dirname, '../src/screenshots-list.js');

try {
  // Read all files in the screenshots directory
  const files = fs.readdirSync(screenshotsDir);
  
  // Filter for image files (optional, but good practice)
  const imageFiles = files.filter(file => /\.(png|jpe?g|gif|webp)$/i.test(file));

  // Create the content for the js file
  const content = `// Auto-generated by scripts/generate-screenshots-list.js
// Do not edit manually. Run 'npm run generate:screenshots' to update.

export const screenshots = ${JSON.stringify(imageFiles, null, 2)};
`;

  // Write to the output file
  fs.writeFileSync(outputFile, content);
  
  console.log(`✅ Successfully generated screenshots list with ${imageFiles.length} images.`);
  console.log(`File saved to: ${outputFile}`);
} catch (error) {
  console.error('❌ Error generating screenshots list:', error.message);
  process.exit(1);
}
