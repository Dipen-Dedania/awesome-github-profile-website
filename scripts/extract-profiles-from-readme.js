/**
 * extract-profiles-from-readme.js
 *
 * Reads README.md, extracts the block between <!-- PROFILES-START --> and
 * <!-- PROFILES-END -->, parses each profile entry, and writes a generated
 * JS module to src/profiles.js.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..');

const readme = readFileSync(join(ROOT, 'README.md'), 'utf-8');

// 1. Extract the block between markers
const startMarker = '<!-- PROFILES-START -->';
const endMarker = '<!-- PROFILES-END -->';
const startIdx = readme.indexOf(startMarker);
const endIdx = readme.indexOf(endMarker);

if (startIdx === -1 || endIdx === -1) {
  console.error('Could not find PROFILES-START / PROFILES-END markers in README.md');
  process.exit(1);
}

const block = readme.slice(startIdx + startMarker.length, endIdx).trim();

// 2. Split by "---" separator
const rawEntries = block.split('---').map(s => s.trim()).filter(Boolean);

// 3. Parse each entry
function slugify(str) {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

function extractOwnerRepo(url) {
  try {
    const u = new URL(url);
    const parts = u.pathname.split('/').filter(Boolean);
    return { owner: parts[0] || null, repo: parts[1] || null };
  } catch {
    return { owner: null, repo: null };
  }
}

const profiles = [];
const seenIds = new Set();

for (const entry of rawEntries) {
  const lines = entry.split('\n').map(l => l.trim()).filter(Boolean);

  let repoUrl = null;
  let siteUrl = null;

  for (const line of lines) {
    if (line.startsWith('Repo -') || line.startsWith('Repo-')) {
      repoUrl = line.replace(/^Repo\s*-\s*/, '').trim();
    } else if (line.startsWith('Link -') || line.startsWith('Link-')) {
      siteUrl = line.replace(/^Link\s*-\s*/, '').trim();
    } else if (line.startsWith('Code -') || line.startsWith('Code-')) {
      // If there's a separate Code line, use it as repoUrl
      repoUrl = line.replace(/^Code\s*-\s*/, '').trim();
    }
  }

  if (!repoUrl && !siteUrl) continue;

  const { owner, repo } = extractOwnerRepo(repoUrl || '');
  const name = repo || owner || siteUrl || 'unknown';
  let id = slugify(name);

  // Ensure unique IDs
  if (seenIds.has(id)) {
    id = `${id}-${owner || Math.random().toString(36).slice(2, 6)}`;
  }
  seenIds.add(id);

  profiles.push({
    id,
    name,
    siteUrl: siteUrl || null,
    repoUrl: repoUrl || null,
    type: 'personal',
    stack: [],
    tags: [],
    author: owner || null,
    country: null,
  });
}

// 4. Write to src/profiles.js
mkdirSync(join(ROOT, 'src'), { recursive: true });

const output = `// Auto-generated by scripts/extract-profiles-from-readme.js
// Do not edit manually — edit README.md instead.

export const profiles = ${JSON.stringify(profiles, null, 2)};
`;

writeFileSync(join(ROOT, 'src', 'profiles.js'), output, 'utf-8');
console.log(`✅ Generated src/profiles.js with ${profiles.length} profiles.`);
